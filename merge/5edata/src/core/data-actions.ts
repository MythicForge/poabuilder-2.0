/**
 * Compute which actions a character has based on their classes, levels, and feats.
 * Returns ComputedAction[] with current uses loaded from storedActionUses.
 */
export function computeActions(
  classes: StoredChar['classes'],
  feats: StoredChar['feats'],
  abilityScores: StoredChar['abilityScores'],
  pb: number,
  storedActionUses: Record<string, number> | undefined,
): ComputedAction[] {
  const result: ComputedAction[] = [];

  for (const def of ACTION_DEFS) {
    let max = 0;
    let className = def.class;

    if (def.class === null && def.feat) {
      // Feat-granted ability — check primary name and any aliases
      const allNames = [def.feat, ...(def.featAliases ?? [])];
      if (!allNames.some(n => feats.includes(n))) continue;
      max = def.maxUses(0, pb, abilityScores);
    } else if (def.class) {
      // Class-granted ability
      const cls = classes.find(c => c.name === def.class);
      if (!cls || cls.level < def.minLevel) continue;
      // Subclass filter
      if (def.subclass && cls.subclass !== def.subclass) continue;
      max = def.maxUses(cls.level, pb, abilityScores);
    } else {
      continue;
    }

    if (max <= 0) continue;

    const current = storedActionUses?.[def.id] ?? max;

    result.push({
      id: def.id,
      name: def.name,
      current,
      max,
      resetOn: def.resetOn,
      description: def.description,
      className,
      hpPool: def.hpPool,
    });
  }

  return result;
}
