import type { StoredChar } from './types';

export interface ActionDef {
  id: string;
  name: string;
  class: string | null;    // class name, null = feat
  subclass?: string;       // full subclass name from registry
  feat?: string;           // feat name filter (primary)
  featAliases?: string[];  // alternate feat names (e.g. TCE vs XPHB hyphen variants)
  minLevel: number;        // minimum class level (0 for feats)
  maxUses: (classLevel: number, pb: number, abil: StoredChar['abilityScores']) => number;
  resetOn: 'short' | 'long';
  description: string;
  hpPool?: boolean;        // pool drawn in variable HP amounts (e.g. Lay on Hands)
}

